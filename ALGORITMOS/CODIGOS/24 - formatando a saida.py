#formatando string com porcentagem

#semelhante a linguagem c o % indica o local que sera mostrado o valor
# e o d indica que sera mostrado um numero inteiro
print("a=%d" % 5)

#mostrando string
print("a=%s" % "abc")

#mostrando float
print ("a=%f" % 5.25)

#outra forma de formatar
print("%s=%f" % ("pi", 3.14))